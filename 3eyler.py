def phi(m):
    """
    Обчислює значення функції Ейлера для заданого m.

    Args:
        m: Целе число.

    Returns:
        Значення функції Ейлера для заданого m.
    """

    def gcd(a, b):
        """
        Обчислює найбільший спільний дільник (НСД) двох цілих чисел a і b.

        Args:
            a: Перше ціле число.
            b: Друге ціле число.

        Returns:
            НСД двох цілих чисел a і b.
        """

        while b != 0:
            a, b = b, a % b
        return a

    # Якщо m <= 1, то функція Ейлера для m дорівнює 0.

    if m <= 1:
        return 0

    # Оскільки кожне просте число ділить m точно один раз, то кількість простих дільників m дорівнює кількості дільників m, які не діляться на квадрат цілого числа, більшого за 1.

    count = 0
    for i in range(2, m + 1):
        # Перевіряємо, чи є i відносно простим з m.
        if gcd(i, m) == 1:
            count += 1

    return count


if __name__ == "__main__":
    m = 18

    # Обчислюємо значення функції Ейлера для m.
    phi_m = phi(m)

    # Виводимо результат.
    print("Значення функції Ейлера для m = %d: %d" % (m, phi_m))
